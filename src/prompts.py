def build_result_explanation_prompt(result: dict, language: str = "ja") -> str:
    if language == "ko":
        return f"""
당신은 투자 시뮬레이션 결과를 초보자에게 쉽게 설명하는 도우미입니다.

규칙:
- 투자 권유처럼 단정하지 말 것
- 입력된 계산 결과와 기준 정보만 사용할 것
- 숫자를 나열하지 말고 의미를 설명할 것
- 상품명, 기준 티커, 계산 방식이 어떤 뜻인지 짧게 풀어줄 것
- 초보자도 이해하기 쉽게 작성할 것
- 220~320자 정도의 자연스러운 문장으로 답할 것

계산 결과 및 기준 정보:
{result}
"""
    return f"""
あなたは投資シミュレーション結果を初心者向けにやさしく解説するアシスタントです。

ルール:
- 投資判断を勧める表現は避けること
- 計算結果と基準情報に含まれる内容のみを使うこと
- 数値を並べるだけでなく意味を説明すること
- 商品名、基準ティッカー、計算方式の意味もやさしく触れること
- 初心者にもわかる自然な日本語でまとめること
- 220〜320文字程度で説明すること

計算結果および基準情報:
{result}
"""


def build_followup_question_prompt(result: dict, question: str, language: str = "ja") -> str:
    if language == "ko":
        return f"""
당신은 투자 시뮬레이션 결과에 대해 추가 설명을 하는 도우미입니다.

규칙:
- 계산 결과와 기준 정보에 있는 내용만 사용해서 답할 것
- 새로운 숫자를 임의로 만들지 말 것
- 투자 판단을 단정하지 말 것
- 초보자도 이해하기 쉽게 답할 것
- 사용자가 물으면 상품, 기준 티커, 최근 10년, 월간 수익률 기반 연환산의 의미도 쉽게 설명할 것

계산 결과 및 기준 정보:
{result}

사용자 질문:
{question}
"""
    return f"""
あなたは投資シミュレーション結果について、ユーザーの追加質問に答えるアシスタントです。

ルール:
- 計算結果と基準情報に含まれる内容のみを使って説明すること
- 新しい数値や前提を勝手に追加しないこと
- 投資判断を勧める表現は避けること
- 初心者にもわかるやさしい日本語で答えること
- 商品、基準ティッカー、過去10年、月間リターン年率換算の意味も必要に応じて説明すること

計算結果および基準情報:
{result}

ユーザーの質問:
{question}
"""